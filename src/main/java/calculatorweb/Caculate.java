package calculatorweb;

public class Caculate {
    public double caculateMinus(double a , double b){
        double result = a - b;
        return result;
    }

    public double caculateSum(double a , double b){
        double result = a + b;
        return result;
    }

    public double calculateDivide(double a , double b){
        double result = a/b;
        return result;
    }

    public double calculateMultiply(double a , double b){
        double result = a*b;
        return result;
    }
}
